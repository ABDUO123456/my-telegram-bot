bot.action('plus', (ctx) => {
    const uid = ctx.from.id;
    if (!userCounts[uid]) userCounts[uid] = 0;
    userCounts[uid]++;
    const count = userCounts[uid];
    let msg = `📿 *السبحة الإلكترونية*\n"سبحان الله وبحمده"`;
    if (count === 33) msg += '\n\n✅ أتممت 33! انتقل إلى: *الحمد لله*';
    if (count === 66) msg += '\n\n✅ أتممت 66! انتقل إلى: *الله أكبر*';
    if (count === 99) msg += '\n\n🌟 أتممت 99! أكمل بـ: *لا إله إلا الله وحده لا شريك له...*';
    ctx.editMessageText(msg, {
        parse_mode: 'Markdown',
        ...Markup.inlineKeyboard([
            [Markup.button.callback(`سبحان الله — العدد: ${count}`, 'plus')],
            [Markup.button.callback('إعادة ضبط 🔄', 'reset')]
        ])
    });
});

