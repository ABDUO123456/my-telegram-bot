// 6. دالة الإرسال (تم تحسينها لتجنب أي خطأ)
async function sendMessages(ctx, list) {
    for (const msg of list) {
        try {
            await ctx.reply(msg); // نستخدم reply العادية لضمان وصول النص كاملاً
            await new Promise(res => setTimeout(res, 1000)); 
        } catch (e) { console.log("Error:", e.message); break; }
    }
}

