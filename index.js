// --- السبحة ---
let count = 0;
bot.hears('📿 السبحة الإلكترونية', (ctx) => {
    count = 0;
    ctx.reply('📿 **السبحة**\nاضغط للتسبيح:', {
        ...Markup.inlineKeyboard([
            [Markup.button.callback(`العدد: ${count}`, 'plus')],
            [Markup.button.callback('إعادة ضبط 🔄', 'reset')]
        ])
    });
});

bot.action('plus', (ctx) => {
    count++;
    ctx.editMessageText('📿 **السبحة**\n"سبحان الله وبحمده"', {
        ...Markup.inlineKeyboard([
            [Markup.button.callback(`العدد: ${count}`, 'plus')],
            [Markup.button.callback('إعادة ضبط 🔄', 'reset')]
        ])
    });
});

bot.action('reset', (ctx) => {
    count = 0;
    ctx.editMessageText('🔄 تم التصفير:', {
        ...Markup.inlineKeyboard([[Markup.button.callback(`العدد: 0`, 'plus')]])
    });
});

bot.launch().then(() => console.log("🚀 بوت أنيس المسلم (النسخة الكاملة) جاهز!"));
