const mainKeyboard = Markup.keyboard([
    ['☀️ أذكار الصباح'],
    ['🌙 أذكار المساء']
]).resize();

// 6. تشغيل الأوامر
bot.start((ctx) => {
    ctx.reply('🌹 مرحباً بك في بوت أذكار المسلم الشامل\nاختر من القائمة بالأسفل:', mainKeyboard);
});

async function sendMessages(ctx, list) {
    for (const msg of list) {
        try {
            await ctx.replyWithMarkdown(msg);
            await new Promise(res => setTimeout(res, 1000)); 
        } catch (e) { console.log("Error sending message"); break; }
    }
}

bot.hears('☀️ أذكار الصباح', (ctx) => sendMessages(ctx, azkar.morning));
bot.hears('🌙 أذكار المساء', (ctx) => sendMessages(ctx, azkar.evening));

// 7. تشغيل البوت
