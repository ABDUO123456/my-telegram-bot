const mainKeyboard = Markup.keyboard([['☀️ أذكار الصباح'], ['🌙 أذكار المساء']]).resize();

bot.start((ctx) => {
    ctx.reply('🌹 مرحباً بك في بوت الأذكار\nاختر ما تريد من القائمة:', mainKeyboard);
});

// 6. دالة الإرسال المحسنة
async function sendMessages(ctx, list) {
    for (const msg of list) {
        try {
            await ctx.replyWithMarkdown(msg); 
            await new Promise(res => setTimeout(res, 800)); // تأخير بسيط لتجنب الحظر
        } catch (e) { console.log("Error:", e.message); break; }
    }
}

bot.hears('☀️ أذكار الصباح', (ctx) => sendMessages(ctx, azkar.morning));
bot.hears('🌙 أذكار المساء', (ctx) => sendMessages(ctx, azkar.evening));

// 7. تشغيل البوت ومعالجة الإغلاق بشكل صحيح
bot.launch()
